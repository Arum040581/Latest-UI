package com.worktracker.vo;

public class TrackerVO {
	
	int years[];
	long years_cal;
	
	int month[];
	long month_cal;
	
	int week[];
	long week_cal;
	
	
	public int[] getYears() {
		return years;
	}

	public void setYears(int[] years) {
		this.years = years;
	}

	public long getYears_cal() {
		return years_cal;
	}

	public void setYears_cal(long years_cal) {
		this.years_cal = years_cal;
	}

	public int[] getMonth() {
		return month;
	}

	public void setMonth(int[] month) {
		this.month = month;
	}

	public long getMonth_cal() {
		return month_cal;
	}

	public void setMonth_cal(long month_cal) {
		this.month_cal = month_cal;
	}

	public int[] getWeek() {
		return week;
	}

	public void setWeek(int[] week) {
		this.week = week;
	}

	public long getWeek_cal() {
		return week_cal;
	}

	public void setWeek_cal(long week_cal) {
		this.week_cal = week_cal;
	}
	

}
